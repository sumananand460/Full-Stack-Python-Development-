# Indexing in python

s = "PythonProgramming"

s = "PythonProgramming"

print(s[0])      # 'P'
print(s[-1])     # 'g' (last character)
print(s[0:6])    # 'Python'
print(s[:6])     # 'Python' (from start)
print(s[6:])     # 'Programming'
print(s[::2])    # Every 2nd character: 'PtoPormig'
print(s[::-1])   # Reverse the string