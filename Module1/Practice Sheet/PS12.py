# Using loops
for i in range(3):
    print("Hello Suman") # Output: Hello Suman -> 3 times

# for loop:
# Bascially you can use for loop when you know how many times to repeat a thing.
for i in range(5):
    print(i) # Output: 0 1 2 3 4

# while loop:
# A while loop is used when the condition is important
i = 1
while i <= 5:
    print(i)
    i += 1 # Output: 1 2 3 4 5