# Using the string method upper() to convert the hello world into upper case 
hello = "hello, world"
print(hello.upper()) # Output: HELLO, WORLD

# Using the strip() method to remove the spaces from the variable word
word = "  Python  "
print(word.strip()) # Output: Python

# Using split() method to separate the words from each other
text = "red blue green"
print(text.split()) # Output: ['red', 'blue', 'green']

# Using join() method to join the words together
text = ["I", "love", "Python"]
result = " ".join(text)
print(result) # Output: I love Python