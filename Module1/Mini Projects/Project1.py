# Program to create a simple calculator
a = int(input("Enter your first number: "))
b = int(input("Enter your second number: "))

sum = a + b
difference = a - b
product = a * b
quotient = a // b
remainder = a % b

print (f"Sum of the two numbers: {sum}")
print (f"Difference of the two numbers: {difference}")
print (f"Product of the two numbers: {product}")
print (f"Quotient of the two numbers: {quotient}")
print (f"Remainder of the two numbers: {remainder}")
